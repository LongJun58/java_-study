public class ArithmeticOperator{
	public static void main(String[] args){
		// /的使用
		System.out.println(10 / 4);
		System.out.println(10.0 / 4);
		double d = 10 / 4;
		System.out.println(d);

		// %的使用，取余数
		System.out.println(10 % 3);
		System.out.println(-10 % 3);
		System.out.println(10 % -3);
		System.out.println(-10 % -3);

	}
}