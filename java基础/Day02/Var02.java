public class Var02{
	public static void main(String[] args){
		// 定义一个人的信息
		int age = 30;
		double score = 88.2;
		char danger = '男';
		String name = "king";
		// 把人的信息输出到窗口
		System.out.println("age = " + age);
		System.out.println("score =" + score);
		System.out.println("danger = "+ danger);
		System.out.println("name = " + name);
	}
}